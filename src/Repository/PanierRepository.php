class PanierRepository extends ServiceEntityRepository
{
    public function __construct(ManagerRegistry $registry)
    {
        parent::__construct($registry, Panier::class);
    }

    /**
     * @throws ORMException
     * @throws OptimisticLockException
     */
    public function add(Panier $entity, bool $flush = true): void
    {
        $this->_em->persist($entity);
        if ($flush) {
            $this->_em->flush();
        }
    }

    /**
     * @throws ORMException
     * @throws OptimisticLockException
     */
    public function remove(Panier $entity, bool $flush = true): void
    {
        $this->_em->remove($entity);
        if ($flush) {
            $this->_em->flush();
        }
    }

    public function FindAllByUser(User $user)
    {
        
        $rsm = new ResultSetMappingBuilder($this->getEntityManager());
        $rsm->addRootEntityFromClassMetadata('App\Entity\Panier', 'Panier');
     
        $sql = 'SELECT * FROM Panier P
        INNER JOIN panier_plants pl
        ON p.id=pl.panier_id
        WHERE p.id = ?';
     
       
        $query = $this->_em->createNativeQuery($sql, $rsm);
        $query->setParameter(1, $user->getPanier());
        return $query->getResult();
     
    }

